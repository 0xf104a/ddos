#ifndef types_h
#define types_h
/*Crosspaltform/crosscompiler types definitions*/

#ifndef _INT64_T
#define _INT64_T
typedef long long int64_t;
#endif

#ifndef _INT32_T
#define _INT32_T
typedef int int32_t;
#endif

#ifndef _INT16_T
#define _INT16_T
typedef short int16_t;
#endif

#ifndef _INT8_T
#define _INT8_T
typedef char int8_t;
#endif

#ifndef _UINT64_T
#define _UINT64_T
typedef unsigned long long uint64_t;
#endif

#ifndef _UINT32_T
#define _UINT32_T
typedef unsigned int uint32_t;
#endif

#ifndef _UINT16_T
#define _UINT16_T
typedef unsigned short uint16_t;
#endif

#ifndef _UINT8_T
#define _UINT8_T
typedef unsigned char uint8_t;
#endif

#endif /* types_h */
